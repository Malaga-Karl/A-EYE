def main() :
	month = 7;  #comment
	if (month == 12 or month == 1 or month == 2) :
		print("Its Winter."); 
	elif (month >= 3 and month <= 5) :
		print("It's Spring."); 
	elif (month >= 6 and month <= 8) :
		print("It's Summer."); 
	else :
		print("It's Autumn."); 
	


if __name__ == '__main__':
    main()